    }

    public function hasAnyRole($roles)
    {
        if (is_array($roles)) {
            foreach ($roles as $role) {
                if ($this->hasRole($role)) {
                    return true;
                }
            }
        } else {
            if ($this->hasRole($roles)) {
                return true;
            }
        }
        return false;
    }

    public function hasRole($rol)
    {
        if ($this->roles()->where('rol', $rol)->first()) {
            return 'hola';
        }
        return false;
    }

}
